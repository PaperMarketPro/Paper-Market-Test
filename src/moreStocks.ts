// Helper to generate a deterministic list of sparkline values ending at the final ltp
function generateSparkline(symbol: string, basePrice: number, change: number): number[] {
  const points = 8;
  const arr: number[] = [];
  
  // Use a simple hash of the symbol for stable pseudo-random variations
  let hash = 0;
  for (let i = 0; i < symbol.length; i++) {
    hash = symbol.charCodeAt(i) + ((hash << 5) - hash);
  }

  // Previous close price calculated from the change percentage
  let current = basePrice / (1 + change / 100);
  
  for (let i = 0; i < points - 1; i++) {
    arr.push(Number(current.toFixed(2)));
    const variationSeed = Math.sin(hash + i) * 0.012; // -1.2% to +1.2% daily variance
    const step = basePrice * variationSeed;
    current += step;
  }
  
  arr.push(Number(basePrice.toFixed(2)));
  return arr;
}

// Convert compact templates to complete Instruments objects
const seenSymbols = new Set<string>();
const uniqueInstruments: Instrument[] = [];

BASE_500_STOCKS.forEach(([symbol, name, basePrice]) => {
  if (seenSymbols.has(symbol)) return;
  seenSymbols.add(symbol);

  // Generate a realistic daily percentage change based on symbol hash (between -4.5% and +4.5%)
  let hash = 0;
  for (let i = 0; i < symbol.length; i++) {
    hash = symbol.charCodeAt(i) + ((hash << 5) - hash);
  }
  
  const rawChange = Math.sin(hash) * 4.5;
  const change = Number(rawChange.toFixed(2));
  
  // High and low calculated relative to the change and basePrice
  const highPercent = Math.abs(change) * 0.008 + 0.005 + (Math.abs(Math.cos(hash)) * 0.015);
  const lowPercent = Math.abs(change) * 0.008 + 0.005 + (Math.abs(Math.sin(hash + 1)) * 0.015);
  
  const high = Number((basePrice * (1 + highPercent)).toFixed(2));
  const low = Number((basePrice * (1 - lowPercent)).toFixed(2));
  
  // Volume scaling according to stock price (higher prices generally have lower volume)
  const volumeMultiplier = basePrice > 10000 ? 5000 : basePrice > 1000 ? 50000 : 250000;
  const volume = Math.round((10 + Math.abs(Math.sin(hash * 2)) * 90) * volumeMultiplier);
  
  const sparkline = generateSparkline(symbol, basePrice, change);

  uniqueInstruments.push({
    symbol,
    name,
    ltp: basePrice,
    change,
    high,
    low,
    volume,
    sparkline
  });
});

export const MORE_100_INSTRUMENTS: Instrument[] = uniqueInstruments;
